    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) teacherTable.getModel();
        
        model.setRowCount(0);
        
        for (People people : organization.getPeopleDirectory().getPeopleList())
        {
            Object[] row = new Object[3];
            row[0] = people.getId();
            row[1] = people.getName();
            row[2] = date;
            model.addRow(row);
        }
    }
    
    private void populateComboBox()
    {
        teacherComboBox.removeAllItems();
        
        for (People people : organization.getPeopleDirectory().getPeopleList()){
            teacherComboBox.addItem(people);
        }     
    }
    
    private void populateRoleComboBox()
    {
        roleComboBox.removeAllItems();
        for (Role role : organization.getPeopleRole()){
            roleComboBox.addItem(role);
        }
    }

