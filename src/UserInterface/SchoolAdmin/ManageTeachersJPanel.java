        People people = (People) teacherComboBox.getSelectedItem();
        Role role = (Role) roleComboBox.getSelectedItem();

        organization.getUserAccountDirectory().createUserAccount(userName, password, null, people, role);

        JOptionPane.showMessageDialog(null, "Teacher Account added successfully.");
        userNameTxt.setText("");
        passwordTxt.setText("");          
